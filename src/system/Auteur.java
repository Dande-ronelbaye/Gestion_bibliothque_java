public void TableAuteur (){
String[] auteur= {"ID","NOM","PRENOM","DATE-NAISS","NATIONALITE"};
String[] affiche= new String[6];
DefaultTableModel model= new DefaultTableModel(null,auteur);
String sql="select*from auteur";
   try {
    Class.forName("com.mysql.jdbc.Driver");
     String url = "jdbc:mysql://localhost:3306/bibliothque";
     con= DriverManager.getConnection(url, "DANDE RONELBAYE", "dande99M");
     Statement st= (Statement) con.createStatement();
     rs=st.executeQuery(sql);
     while(rs.next()){
         affiche [0]=rs.getNString("id");
         affiche [1]=rs.getNString("nom");
         affiche [2]=rs.getNString("prenom");
         affiche [3]=rs.getNString("date-naiss");
         affiche [4]=rs.getNString("nationalite");
         model.addRow(affiche);   
     }
     jTable1.setModel(model);
     con.close();
   }catch(Exception e){e.printStackTrace();}
}
